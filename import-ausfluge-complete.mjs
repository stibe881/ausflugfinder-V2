async function importAusfluge() {
  const connection = await pool.getConnection();

  try {
    // First, delete existing imported trips
    console.log("Deleting previous ausfluge imports...");
    await connection.query(
      "DELETE FROM trips WHERE userId = 1 AND id >= 11"
    );

    console.log(`Importing ${ausfluge.length} ausflüge with complete data...`);

    let successful = 0;
    let failed = 0;

    for (const trip of ausfluge) {
      try {
        const sql = `
          INSERT INTO trips (
            title,
            description,
            destination,
            region,
            category,
            cost,
            latitude,
            longitude,
            isPublic,
            userId,
            startDate,
            endDate,
            status,
            durationMin,
            durationMax,
            distanceMin,
            distanceMax,
            ageRecommendation,
            niceToKnow
          ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, NOW(), DATE_ADD(NOW(), INTERVAL 1 DAY), 'planned', ?, ?, ?, ?, ?, ?)
        `;

        await connection.query(sql, [
          trip.title,
          trip.description,
          trip.destination,
          trip.region,
          trip.category,
          trip.cost,
          trip.latitude,
          trip.longitude,
          trip.isPublic,
          1, // userId = 1
          trip.durationMin,
          trip.durationMax,
          trip.distanceMin,
          trip.distanceMax,
          trip.ageRecommendation,
          trip.niceToKnow,
        ]);

        successful++;
        console.log(`✓ ${trip.title}`);
      } catch (error) {
        failed++;
        console.error(`✗ ${trip.title}: ${error.message}`);
      }
    }

    console.log(`\n✅ Import complete! Successful: ${successful}/${ausfluge.length}`);
    if (failed > 0) console.log(`⚠️  Failed: ${failed}`);
  } catch (error) {
    console.error("Import failed:", error);
  } finally {
    await connection.release();
    await pool.end();
  }
}

importAusfluge();
